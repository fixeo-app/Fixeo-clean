/* ============================================================
   FIXEO — Phone Utilities (WhatsApp-First Auth Layer)
   js/fixeo-phone-utils.js  v1a

   PURPOSE:
     Shared helpers for phone normalization and synthetic email
     generation. Used by auth.html, fixeo-auth-supabase.js,
     fixeo-supabase-core.js, and auth-global.js.

   SYNTHETIC EMAIL FORMAT:
     +212660484415  →  212660484415@fixeo.ma
     Deterministic. Reversible. Never shown to users.
     Domain: fixeo.ma (no MX mailboxes — never deliverable).

   SUPPORTED INPUT FORMATS (Morocco):
     0660484415      06-series
     0700000000      07-series
     +212660484415   E.164 with +
     +212700000000   E.164 with +
     2126XXXXXXXX    E.164 without +
     2127XXXXXXXX    E.164 without +
     06 60 48 44 15  spaces accepted and stripped
     06-60-48-44-15  dashes accepted and stripped

   NORMALIZED OUTPUT:
     +212XXXXXXXXX   (12 digits after +, always)

   ============================================================ */
(function (window) {
  'use strict';

  /* ── SYNTHETIC EMAIL DOMAIN ──────────────────────────────── */
  var SYNTHETIC_DOMAIN = 'fixeo.ma';

  /* ── SYNTHETIC EMAIL MARKER ─────────────────────────────── */
  /* A synthetic email is a 12-digit number @ fixeo.ma.
     Format: /^\d{12}@fixeo\.ma$/ */
  var RE_SYNTHETIC = /^(\d{12})@fixeo\.ma$/;

  /* ── MOROCCO PHONE VALIDATION ──────────────────────────── */
  /* After normalization: +212[67]\d{8} = +212 + 1(6or7) + 8 digits = 12 chars total */
  var RE_NORMALIZED = /^\+212[67]\d{8}$/;

  /* ── normalizePhone ────────────────────────────────────── */
  /**
   * Normalize any supported Moroccan phone format to E.164 (+212XXXXXXXXX).
   *
   * @param  {string} raw  — raw user input
   * @returns {string|null} — normalized E.164 string, or null if invalid
   */
  function normalizePhone(raw) {
    if (!raw) return null;

    /* Strip all whitespace, dashes, dots, parentheses */
    var v = String(raw).replace(/[\s\-\.\(\)]/g, '');

    /* Strip leading + (we'll add it back) */
    if (v.charAt(0) === '+') v = v.slice(1);

    /* Strip leading 00 (international dialing prefix) */
    if (v.slice(0, 3) === '002') v = v.slice(2); /* 00212... → 212... */

    /* Starts with 212 (country code without +) → prepend + */
    if (/^212[67]\d{8}$/.test(v)) return '+' + v;

    /* Starts with 06 → replace leading 0 with 212 */
    if (/^06\d{8}$/.test(v)) return '+212' + v.slice(1);

    /* Starts with 07 → replace leading 0 with 212 */
    if (/^07\d{8}$/.test(v)) return '+212' + v.slice(1);

    return null; /* unrecognized format */
  }

  /* ── isPhoneIdentifier ─────────────────────────────────── */
  /**
   * Returns true if the input looks like a phone number
   * (no @ character, and matches phone patterns after stripping).
   *
   * @param  {string} input
   * @returns {boolean}
   */
  function isPhoneIdentifier(input) {
    if (!input) return false;
    var s = String(input).trim();
    /* Email addresses always contain @ */
    if (s.indexOf('@') !== -1) return false;
    /* Must contain digits and optionally +, spaces, dashes */
    if (!/^[\d\s\+\-\.\(\)]+$/.test(s)) return false;
    /* Must normalize to a valid Moroccan phone */
    return normalizePhone(s) !== null;
  }

  /* ── phoneToSyntheticEmail ─────────────────────────────── */
  /**
   * Generate a deterministic internal email from a normalized phone.
   *
   * +212660484415 → 212660484415@fixeo.ma
   *
   * The + is stripped. The result is never displayed to users.
   * It is used ONLY as the Supabase Auth email credential.
   *
   * @param  {string} normalizedPhone — result of normalizePhone()
   * @returns {string|null}
   */
  function phoneToSyntheticEmail(normalizedPhone) {
    if (!normalizedPhone) return null;
    var digits = String(normalizedPhone).replace(/^\+/, '');
    if (!/^\d{12}$/.test(digits)) return null;
    return digits + '@' + SYNTHETIC_DOMAIN;
  }

  /* ── isSyntheticEmail ──────────────────────────────────── */
  /**
   * Returns true if email was generated by phoneToSyntheticEmail().
   *
   * Used to prevent synthetic emails from being displayed to users
   * or used as display identifiers.
   *
   * @param  {string} email
   * @returns {boolean}
   */
  function isSyntheticEmail(email) {
    if (!email) return false;
    return RE_SYNTHETIC.test(String(email));
  }

  /* ── syntheticEmailToPhone ─────────────────────────────── */
  /**
   * Reverse of phoneToSyntheticEmail().
   * Recovers the normalized E.164 phone from a synthetic email.
   *
   * 212660484415@fixeo.ma → +212660484415
   *
   * @param  {string} email
   * @returns {string|null}
   */
  function syntheticEmailToPhone(email) {
    if (!email) return null;
    var m = String(email).match(RE_SYNTHETIC);
    if (!m) return null;
    return '+' + m[1];
  }

  /* ── phoneFromRaw ──────────────────────────────────────── */
  /**
   * Convenience: normalize raw phone input and generate synthetic email.
   * Returns null for both if phone is invalid.
   *
   * @param  {string} raw
   * @returns {{ normalized: string|null, syntheticEmail: string|null }}
   */
  function phoneFromRaw(raw) {
    var normalized = normalizePhone(raw);
    var syntheticEmail = normalized ? phoneToSyntheticEmail(normalized) : null;
    return { normalized: normalized, syntheticEmail: syntheticEmail };
  }

  /* ── formatPhoneForDisplay ─────────────────────────────── */
  /**
   * Format a normalized phone for user display.
   * +212660484415 → 06 60 48 44 15
   * Returns raw input unchanged if not a recognized format.
   *
   * @param  {string} phone — normalized or raw
   * @returns {string}
   */
  function formatPhoneForDisplay(phone) {
    if (!phone) return '';
    var v = String(phone).replace(/[\s\-]/g, '');
    /* +212 6/7 XXXXXXXX → 06 XX XX XX XX */
    var m = v.match(/^\+212([67])(\d{2})(\d{2})(\d{2})(\d{2})$/);
    if (m) return '0' + m[1] + ' ' + m[2] + ' ' + m[3] + ' ' + m[4] + ' ' + m[5];
    return phone;
  }

  /* ── Public API ────────────────────────────────────────── */
  window.FixeoPhoneUtils = {
    version:               '1a',
    normalizePhone:        normalizePhone,
    isPhoneIdentifier:     isPhoneIdentifier,
    phoneToSyntheticEmail: phoneToSyntheticEmail,
    isSyntheticEmail:      isSyntheticEmail,
    syntheticEmailToPhone: syntheticEmailToPhone,
    phoneFromRaw:          phoneFromRaw,
    formatPhoneForDisplay: formatPhoneForDisplay
  };

  /* Expose at global scope for inline script usage in auth.html */
  window._fxPhone = window.FixeoPhoneUtils;

})(window);
