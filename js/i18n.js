class I18n {
  constructor() {
    this.lang = localStorage.getItem('fixeo_lang') || 'fr';
    this.translations = FIXEO_TRANSLATIONS;
  }
  t(key, params = {}) {
    const dict = this.translations[this.lang] || this.translations.fr;
    let str = dict[key] || key;
    Object.keys(params).forEach(k => { str = str.replace(`{${k}}`, params[k]); });
    return str;
  }
  setLang(lang) {
    if (!['fr','ar','en'].includes(lang)) return;
    this.lang = lang;
    localStorage.setItem('fixeo_lang', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.body.classList.toggle('rtl', lang === 'ar');
    this.applyTranslations();
    document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
  }
  applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const attr = el.getAttribute('data-i18n-attr');
      const text = this.t(key);
      if (attr) { el.setAttribute(attr, text); }
      else { el.textContent = text; }
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      el.innerHTML = this.t(el.getAttribute('data-i18n-html'));
    });
    const sel = document.querySelector('#lang-select');
    if (sel) sel.value = this.lang;
  }
  getCurrentLang() { return this.lang; }
}

window.i18n = new I18n();
document.addEventListener('DOMContentLoaded', () => {
  window.i18n.applyTranslations();
});
